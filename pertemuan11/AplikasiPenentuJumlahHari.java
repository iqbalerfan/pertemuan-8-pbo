    private void hitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hitungActionPerformed
        if (display.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Error: Isian tahun kosong!", "Error: Isian tahun kosong!", JOptionPane.WARNING_MESSAGE);
        } else {
           try {
            int tahun = Integer.parseInt(display.getText());
            String bulan = bulanComboBox.getSelectedItem().toString();
            
            // Membuat objek HitungHari dan memanggil method hitung()
            HitungHari hitungHari = new HitungHari(tahun, bulan);
            String hasil = hitungHari.hitung();
            
            // Menampilkan hasil
            JumlahHariLabel.setText(hasil);
            hapus.setEnabled(true);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Error: Input tahun harus berupa angka!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
            
            
            

        
    

    }//GEN-LAST:event_hitungActionPerformed

